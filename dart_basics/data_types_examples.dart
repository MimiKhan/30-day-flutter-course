void main() {
  // Example 1: Integer (int) for whole numbers
  int age = 25;
  print('Age: $age');

  // Example 2: Double for decimal numbers
  double price = 19.99;
  print('Price: \$${price}');

  // Example 3: String for text
  String greeting = 'Hello, Flutter!';
  print('Greeting: $greeting');

  // Example 4: Boolean for true/false
  bool isBeginner = true;
  print('Is beginner? $isBeginner');
}