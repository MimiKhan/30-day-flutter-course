void main() {
  // Example 1: Arithmetic operators
  int a = 10;
  int b = 5;
  print('Addition: ${a + b}'); // 15
  print('Subtraction: ${a - b}'); // 5

  // Example 2: Multiplication and division
  double x = 7.5;
  double y = 2.0;
  print('Multiplication: ${x * y}'); // 15.0
  print('Division: ${x / y}'); // 3.75

  // Example 3: Comparison operators
  int score = 80;
  print('Is score equal to 80? ${score == 80}'); // true
  print('Is score greater than 90? ${score > 90}'); // false

  // Example 4: Logical operators
  bool hasTicket = true;
  bool hasID = false;
  print('Can enter? ${hasTicket && hasID}'); // false
}